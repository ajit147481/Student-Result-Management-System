        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Raut@358");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student inner join result where student.rollno='"+rollno+"' && result.rollno='"+rollno+"'" );
            if(rs.next()){
                jTextField1.setText(rs.getString(2));
                jTextField2.setText(rs.getString(3));
                jTextField3.setText(rs.getString(4));
                jTextField4.setText(rs.getString(5));
                jTextField5.setText(rs.getString(6));
                jTextField7.setText(rs.getString(7));
                
                jTextField8.setText(rs.getString(9));
                jTextField9.setText(rs.getString(10));
                jTextField10.setText(rs.getString(11));
                jTextField11.setText(rs.getString(12));
                jTextField12.setText(rs.getString(13));
                
                int arr[]=new int[5];
                arr[0]=Integer.parseInt(jTextField8.getText());
                arr[1]=Integer.parseInt(jTextField9.getText());
                arr[2]=Integer.parseInt(jTextField10.getText());
                arr[3]=Integer.parseInt(jTextField11.getText());
                arr[4]=Integer.parseInt(jTextField12.getText());
                
                String result="pass";
                int total=0;
                float percentage;
                for(int i=0;i<5;i++){
                    total+=arr[i];
                    if(arr[i]<35){
                        result="Fail";
                    }
                }
                percentage=total/5;
                jTextField13.setText(Integer.toString(total));
                jTextField14.setText(Float.toString(percentage));
                jTextField15.setText(result);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
