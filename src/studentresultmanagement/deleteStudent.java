    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        String rollno=jTextField1.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Raut@358");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select* from student where rollno='"+rollno+"'");
            if(rs.next()){
                String query="delete from student where rollno='"+rollno+"'";
                String query1="delete from result where rollno='"+rollno+"'";
                st.executeUpdate(query);
                st.executeUpdate(query1);
                JOptionPane.showMessageDialog(null,"Student data deleted");
                setVisible(false);
                new deleteStudent().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Roll number Does Not Exist ");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton8ActionPerformed

