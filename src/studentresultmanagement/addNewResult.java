    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        String rollno=jTextField9.getText();
        String OS=jTextField10.getText();
        String CP=jTextField11.getText();
        String Micro=jTextField12.getText();
        String DS= jTextField13.getText();
        String DBMS= jTextField14.getText();
        //String gender=(String) jComboBox3.getSelectedItem();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Raut@358");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result(rollno,OS,CP,Micro,DS,DBMS) values('"+rollno+"','"+OS+"','"+CP+"','"+Micro+"','"+DS+"','"+DBMS+"' )");
                JOptionPane.showMessageDialog(null, "Result Added Successfully");
                setVisible(false);
                new addNewResult().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Roll Number is not Present ");
                setVisible(false);
                new addNewResult().setVisible(true);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton8ActionPerformed

